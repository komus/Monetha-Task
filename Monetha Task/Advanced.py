import itertools

targets = {"#": 5, "O": 3, "X": 1, "!": -1, "!!": -3, "!!!": -5}


def check_score(regular_list):
    totalscore = 0
    # Check whether the parameter is a list
    flat_list = list(itertools.chain(*regular_list))
    try:
        for v in flat_list:
            totalscore += targets[v]
    except:
        return "Exception occurred"

    return 0 if totalscore < 0 else totalscore

